x=int(input())
#Arithmetic operator
print(x+5)#addition
print(x-5)#Substraction
print(x*5)#Multiplication
print(x/5)#Division
print(x%5)#Modulus
#Bitwise Operator
print(x&5)#And
print(x|5)#Or
print(~x)#Not
print(x^5)#Xor
print(x>>5)#Right shift
print(x<<5)#left shift
#boolian
print(x>10)
print(x>3)
